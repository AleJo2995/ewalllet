  SELECT [dbo].[usuario].[cedula]
      ,[password]
      ,[dbo].[usuario].[nombre]
      ,[primer_apellido]
      ,[segundo_apellido]
      ,[correo]
      ,[fecha_nacimiento]
      ,[activo]
  FROM [dbo].[usuario]
  INNER JOIN [dbo].[rol_x_usuario]
    ON [dbo].[rol_x_usuario].[cedula] = [dbo].[usuario].[cedula]
  INNER JOIN [dbo].[rol]
    ON [dbo].[rol].[id] = [dbo].[rol_x_usuario].[id_rol]
  WHERE [dbo].[rol].[nombre] = 'Chofer'